    def is_function_answering_yes_on_sample(self, sample):
        for conjunction in self.ideal_function:
            if self.is_conjunction_answering_yes_on_sample(conjunction, sample):
                return True

        return False

    def is_conjunction_answering_yes_on_sample(self, conjunction, sample):
        index = -1
        for i in conjunction:
            index += 1
            if i is 0:
                continue
            if i != sample[index]:
                return False

        return True
